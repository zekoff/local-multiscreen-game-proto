import { chromium } from 'playwright';
const BASE = 'http://localhost:3000';
const { code } = await (await fetch(`${BASE}/api/rooms`, { method: 'POST' })).json();
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1280, height: 720 } });
const errs = [];
page.on('pageerror', (e) => errs.push(e.message));
await page.goto(`${BASE}/mainscreen.html?room=${code}`);
await page.waitForTimeout(900);
await page.fill('#ship-name', 'Nifty Fern');
await page.click('#launch-btn', { force: true });
await page.waitForTimeout(1500);
const h1 = await page.textContent('header h1');
const log = await page.textContent('#log');
console.log('h1:', h1);
console.log('log has name:', /Nifty Fern/.test(log));
console.log('errors:', errs.length);
await page.screenshot({ path: '/tmp/wp7-named.png' });
await browser.close();
process.exit(h1.includes('Nifty Fern') && /Nifty Fern/.test(log) && errs.length === 0 ? 0 : 1);
